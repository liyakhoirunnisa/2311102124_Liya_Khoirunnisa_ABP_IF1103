<script setup>
import { ref, onMounted } from 'vue'

// Variabel reaktif untuk menyimpan teks yang sedang diketik
const typedText = ref('')

// Daftar kata yang akan ditampilkan bergantian
const words = ['Frontend Dev', 'Backend Dev', 'Problem Solver']

let wordIndex = 0
let charIndex = 0
let isDeleting = false

// Fungsi rekursif untuk mengatur efek mengetik
const typeEffect = () => {
  const currentWord = words[wordIndex]

  // Logika menghapus atau mengetik
  if (isDeleting) {
    typedText.value = currentWord.substring(0, charIndex - 1)
    charIndex--
  } else {
    typedText.value = currentWord.substring(0, charIndex + 1)
    charIndex++
  }

  // Mengatur kecepatan animasi (lebih cepat saat menghapus)
  let typeSpeed = isDeleting ? 50 : 100

  // Logika saat kata selesai diketik atau selesai dihapus
  if (!isDeleting && charIndex === currentWord.length) {
    typeSpeed = 2000 // Jeda 2 detik sebelum mulai menghapus
    isDeleting = true
  } else if (isDeleting && charIndex === 0) {
    isDeleting = false
    wordIndex = (wordIndex + 1) % words.length // Pindah ke kata berikutnya
    typeSpeed = 500 // Jeda 0.5 detik sebelum mengetik kata baru
  }

  // Memanggil diri sendiri secara berulang
  setTimeout(typeEffect, typeSpeed)
}

onMounted(() => {
  // Mulai animasi saat komponen selesai dirender
  typeEffect()
})
</script>

